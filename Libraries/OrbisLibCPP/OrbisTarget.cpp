bool OrbisTarget::OpenDatabase(sqlite3** db)
{
	char* ErrorMsg = 0;
	int rc = 0;

	rc = sqlite3_open(this->DBPath, db);
	if (rc != SQLITE_OK)
		return false;

	sqlite3_busy_timeout(*db, 1000);

	return true;
}

bool OrbisTarget::DoesTargetExist(const char* TargetName)
{
	sqlite3* db;
	char* ErrorMsg = 0;
	int rc = 0;
	bool Result = false;

	if (!OpenDatabase(&db))
	{
		printf("Failed to open database: %s\n", sqlite3_errmsg(db));

		return false;
	}

	sqlite3_stmt *stmt;
	rc = sqlite3_prepare_v2(db, "SELECT COUNT(*) FROM Targets WHERE TargetName=?", -1, &stmt, NULL);
	if (rc != SQLITE_OK)
	{
		printf("Failed to prep stmt: %s\n", sqlite3_errmsg(db));

		sqlite3_close(db);
		return false;
	}

	rc = sqlite3_bind_text(stmt, 1, TargetName, -1, SQLITE_TRANSIENT);
	if (rc != SQLITE_OK)
	{
		printf("Failed to bind text: %s\n", sqlite3_errmsg(db));

		sqlite3_close(db);
		return false;
	}

	rc = sqlite3_step(stmt);
	if (rc != SQLITE_ERROR)
	{
		if (sqlite3_column_int(stmt, 0) > 0)
			Result = true;
		else
			Result = false;
	}
	else
	{
		printf("Failed to read row: %s\n", sqlite3_errmsg(db));

		Result = false;
	}

	sqlite3_finalize(stmt);
	sqlite3_close(db);

	return Result;
}

